package mymath

import "math"

func Abs(x float64) float64 {
	return math.Abs(x)
}

func Max(x, y float64) float64 {
	return math.Max(x, y)
}

func Sqrt(x float64) float64 {
	return math.Sqrt(x)
}

func Yn(n int, x float64) float64 {
	return math.Yn(n, x)
}

func Acos(x float64) float64 {
	return math.Acos(x)
}

func Acosh(x float64) float64 {
	return math.Acosh(x)
}

func Asin(x float64) float64 {
	return math.Asin(x)
}

